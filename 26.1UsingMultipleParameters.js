// using multiple parameters
// You can define multiple parameters for a function by comma-separating them.
function sayHello(name, age) {
  console.log(name + " is " + age + " years old.");
}

// contoh 2
function test(x, y) {
  if (x > y) {
    console.log(x);
  } else {
    console.log(y);
  }
}
test(5, 8);

